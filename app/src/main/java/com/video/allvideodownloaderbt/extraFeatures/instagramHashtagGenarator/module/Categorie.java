package com.video.allvideodownloaderbt.extraFeatures.instagramHashtagGenarator.module;


public class Categorie {
    int id;
    String name;


    public Categorie(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }
}
